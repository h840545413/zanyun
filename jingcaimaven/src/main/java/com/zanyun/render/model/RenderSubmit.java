package com.zanyun.render.model;

public class RenderSubmit {
	private String custom_name;
	private String pic_width;
	private String pic_height;
	private String precision_level;
	private String pic_spherical;
	
	public String getCustom_name() {
		return custom_name;
	}
	public void setCustom_name(String custom_name) {
		this.custom_name = custom_name;
	}
	public String getPic_width() {
		return pic_width;
	}
	public void setPic_width(String pic_width) {
		this.pic_width = pic_width;
	}
	public String getPic_height() {
		return pic_height;
	}
	public void setPic_height(String pic_height) {
		this.pic_height = pic_height;
	}
	public String getPrecision_level() {
		return precision_level;
	}
	public void setPrecision_level(String precision_level) {
		this.precision_level = precision_level;
	}
	public String getPic_spherical() {
		return pic_spherical;
	}
	public void setPic_spherical(String pic_spherical) {
		this.pic_spherical = pic_spherical;
	}

}
